# To demonstrate, let's create a variable called "box" and put the number 5 in it.
box = 5

# Now, let's print the contents of the box to see what's inside.
print(box) # 5

# Sometimes, we might want to change what's inside the box.
# Let's change the contents of the box to 10.
box = 10

# Now, let's print the contents of the box again.
print(box) # 10

# We can also put other things in the box, like text.
# but lets be more descriptive, because 'box' is pretty vague.

# Let's create a variable called "name" and put the text "John" in it.
name = "John"

# Now, let's print the contents of the name variable.
print(name) # John